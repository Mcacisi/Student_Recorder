    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String studNo, initials, surname, module, Admission;
        int mark1 = 0;
        int mark2 = 0;
        int mark3 = 0;
        double avg;
        double perc;
        int finalMark;

        studNo = txtStudentNo.getText().trim();
        if (studNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter student number", "Student Number Missing", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        initials = txtInitials.getText().trim().toUpperCase();
        if (initials.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter initials", "missing information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        surname = txtSurname.getText().trim().toUpperCase();
        if (surname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter surname", "missing information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        module = (cmbModule.getSelectedItem()).toString();

        try {
            mark1 = parseInt(txtMark1.getText().trim());
           
            if (mark1 < 0 || mark1 > 100) {
                JOptionPane.showMessageDialog(this, "Mark must be between 0 to 100", "Input Error", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter only numbers", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            mark2 = parseInt(txtMark2.getText().trim());
            if (mark2 < 0 || mark2 > 100) {
                JOptionPane.showMessageDialog(this, "Mark must be between 0 to 100", "Input Error", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter only numbers", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            mark3 = parseInt(txtMark3.getText().trim());
            if (mark3 < 0 || mark3 > 100) {
                JOptionPane.showMessageDialog(this, "Mark must be between 0 to 100", "Input Error", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter only numbers", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        // Overall mark calculation to check Exam admission
        avg = (mark1 + mark2 + mark3)/300.0;
        perc = avg * 100.0;
        
        finalMark = (int)Math.round(perc);
        if (finalMark < 50) {
            Admission = "No";
        } else {
            Admission = "yes";
        }

        
        for(Student d : arrStudent){
            if(d.getStudNo().equals(studNo) && d.getModule().equals(module)){
                JOptionPane.showMessageDialog(this, "Student number already exists for this module", "Duplicate students", JOptionPane.OK_OPTION);
                return;
            }
        }
        
        Student objStud = new Student(studNo,initials,surname,module,mark1,mark2,mark3,finalMark,Admission);
        
        arrStudent.add(objStud);
        JOptionPane.showMessageDialog(this, "Student added successfully", "Data input", JOptionPane.INFORMATION_MESSAGE);
        
        try
           {
            ObjectOutputStream out = new ObjectOutputStream(new FileOutputStream("studentFile.dat"));
            out.writeObject(arrStudent);
            JOptionPane.showMessageDialog(this, "Student added to fileðŸ˜ŽðŸ‘");
            out.close();
            
           }catch (IOException e){
               JOptionPane.showMessageDialog(this, "There is an issue in saving data to file", "File Handling Error", JOptionPane.ERROR_MESSAGE);
           }
    }//GEN-LAST:event_btnAddActionPerformed

    
